package day01;

public class Ex02 {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;
        System.out.printf("x: %d, y: %d%n", x, y);
        System.out.printf("x + y: %d%n", x + y);
        System.out.printf("x - y: %d%n", x - y);
        System.out.printf("x * y: %d%n", x * y);
        System.out.printf("x / y: %.2f%n", x / (double) y);
        System.out.printf("x %% y: %d%n", x % y);
    }
}
